function sizeUp() {
	
	document.getElementById("clockbox").style.fontSize = "10vw";
}

function sizeDown() {

	document.getElementById("clockbox").style.fontSize = "3vw";
}

function changeColor() {

	document.getElementById("clockbox").style.color = "red";
}

function addShadow() {

	document.getElementById("clockbox").style.textShadow = "5px 5px green";
}